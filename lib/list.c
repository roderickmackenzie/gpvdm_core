void list_free(struct list *in)
{
	int i=0;

	for (i=0;i<in->len;i++)
	{
		free(in->names[i]);
	}

	free(in->names);
}

int list_cmp(struct list *in,char *name)
{
	int i=0;

	for (i=0;i<in->len;i++)
	{
		if (strcmp(name,in->names[i])==0)
		{
			return 0;
		}
	}

return -1;
}

void list_dump(struct list *in)
{
	int i=0;

	for (i=0;i<in->len;i++)
	{
		printf("%s\n",in->names[i]);
	}

}
